package ciclos;

public class WhileLoop_ejemplo {

	public static void main(String[] args) {
		int a = 7;
		
		//Va a salir del ciclo While cuando "a" es mayor que 4
		while(a<=6) {
			System.out.println("el valor de a:" + a);
			//incrementa el valor en 1 para la siguiente iteracion
			a++;
		}
		
	}

}
